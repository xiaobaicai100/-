function [w] = PTS(A,d)
%A为一个储存系数矩阵系数的矩阵，A的每一行分别代表a，b,c.
%d代表线性方程组增广矩阵的最后一列
m=length(d);
d1=zeros(1,m);
v1=[0,0]';
v2=[1,0]';
v3=[0,1]';
[x]=TA(A,v1,d);
[y]=TA(A,v2,d1);
[z]=TA(A,v3,d1);
D=(1-y(m+1))*(1-z(2))-y(2)*z(m+1);
r=(x(m+1)*(1-z(2))+x(2)*z(m+1))/D;
s=(x(m+1)*y(2)+x(2)*(1-y(m+1)))/D;
w=x+r*y+s*z;
%w=w1(1,1:m+1)
end
%d是n维的则输出n+1维的
